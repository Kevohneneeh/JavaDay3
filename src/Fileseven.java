public class Fileseven {
    public static void main(String[] args) {
        //Loops in JAVA
        int x, y;
        x = 0;
        while (x<5){
            System.out.println(x);
            x++;
        }
        //Do while
        y =0;
        do {
            System.out.println(y);
            y++;
        }while (y<5);
        //For loop
        for (int i = 0; i<5;i++){
            System.out.println(i);
        }
    }
}
